package com.agranee.test.vo;

public class CoinVO {

	// It will be stored in dollar
	// If user adds 50 cent coin then it will add 0.5
	float value;
	String type;

	/**
	 * @return the value
	 */
	public float getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(float value) {
		this.value = value;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	
	
	
}
